
public class Encapsulate {

    private String name;
    private int rollNo;
    private int age;

    public int getAge() {
        return age;
    }

    public void setAge(int newAge) {
        age = newAge;
    }

    public String getName() {
        return name;
    }

    public void setName(String newName) {
        name = newName;
    }

    public int getRollNo() {
        return rollNo;
    }

    public void setRollNo(int newRollNo) {
        rollNo = newRollNo;
    }
}

